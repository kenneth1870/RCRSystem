    public void agregarBulto() {
        try {
            float p = Float.parseFloat(txtPesoBulto.getText());
            txtPesoBulto.setBorder(BorderFactory.createLineBorder(Color.GRAY));
            txtPesoBulto.setToolTipText("");
            if (txtPesoBulto.getText().equals("")) {
                txtPesoBulto.setBorder(BorderFactory.createLineBorder(Color.red));
                txtPesoBulto.setToolTipText("No se permiten espacios vacios");
                JOptionPane.showMessageDialog(null, "No se permiten espacios vacios. Ingrese el peso");
            } else if (p < 10) {
                txtPesoBulto.setBorder(BorderFactory.createLineBorder(Color.red));
                txtPesoBulto.setToolTipText("El peso debe ser mayor a 10");
                JOptionPane.showMessageDialog(null, "Ingrese un peso mayor a 10");
            } else {
                controlador.agregar_bulto();
            }

        } catch (NumberFormatException e) {
            txtPesoBulto.setBorder(BorderFactory.createLineBorder(Color.red));
            txtPesoBulto.setToolTipText("Solo se permiten números");
            JOptionPane.showMessageDialog(null, "Solo se permiten números. Ingrese un peso válido");
        }
    }

    public void confirmar() {
       this.txtPesoBulto.setBorder(BorderFactory.createLineBorder(Color.GRAY));
         this.txtPesoBulto.setToolTipText("");
        txtPlacaVehiculo.setBorder(BorderFactory.createLineBorder(Color.GRAY));
        txtPlacaVehiculo.setToolTipText("");
        txtChofer.setBorder(BorderFactory.createLineBorder(Color.GRAY));
        txtChofer.setToolTipText("");
        if (txtChofer.getText().equals("")) {
            txtChofer.setBorder(BorderFactory.createLineBorder(Color.red));
            txtChofer.setToolTipText("No se permiten espacios vacios. Ingrese el chofer");
            JOptionPane.showMessageDialog(null, "No se permiten espacios vacios");
        } else if (txtPlacaVehiculo.getText().equals("")) {
            txtPlacaVehiculo.setBorder(BorderFactory.createLineBorder(Color.red));
            txtPlacaVehiculo.setToolTipText("No se permiten espacios vacios");
            JOptionPane.showMessageDialog(null, "No se permiten espacios vacios. Ingrese la placa del vehículo");
        } else {

            int i = controlador.confirmar_compra();
            switch (i) {
                case 0:
                    JOptionPane.showMessageDialog(null, "Agregue bultos comprados para confimar una compra");
                    break;
                case 2:
                    JOptionPane.showMessageDialog(null, "No se pudo conectar a la Base de Datos, "
                            + "verifique que se encuentre el linea", "Error de Conexion", JOptionPane.ERROR_MESSAGE);
                    break;
                default:
                    cerrar();
                    try {
                        controlador.borrar_todo();
                    } catch (Exception ex) {
                        Logger.getLogger(VentIngresarRegCompra.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    break;
            }
        }
    }

